# =================================================================
# userret: 内核返回用户态的终极跳板
# C 语言调用: userret((uint64)p->trapframe);
# 此时 a0 寄存器里装的是 trapframe 的内存地址
# =================================================================
    .globl     userret
    .align     4
userret:
# 此时 a0 是 trapframe 的基地址
# 我们要对照 trapframe 的结构，把所有寄存器装填回 CPU

# 恢复返回地址 (ra)
    csrw       satp, a1
    sfence.vma zero, zero

    ld         t0, 544(a0)
    fscsr      t0
    fld        f0, 288(a0)
    fld        f1, 296(a0)
    fld        f2, 304(a0)
    fld        f3, 312(a0)
    fld        f4, 320(a0)
    fld        f5, 328(a0)
    fld        f6, 336(a0)
    fld        f7, 344(a0)
    fld        f8, 352(a0)
    fld        f9, 360(a0)
    fld        f10, 368(a0)
    fld        f11, 376(a0)
    fld        f12, 384(a0)
    fld        f13, 392(a0)
    fld        f14, 400(a0)
    fld        f15, 408(a0)
    fld        f16, 416(a0)
    fld        f17, 424(a0)
    fld        f18, 432(a0)
    fld        f19, 440(a0)
    fld        f20, 448(a0)
    fld        f21, 456(a0)
    fld        f22, 464(a0)
    fld        f23, 472(a0)
    fld        f24, 480(a0)
    fld        f25, 488(a0)
    fld        f26, 496(a0)
    fld        f27, 504(a0)
    fld        f28, 512(a0)
    fld        f29, 520(a0)
    fld        f30, 528(a0)
    fld        f31, 536(a0)

    ld         ra, 40(a0)
# 恢复用户栈指针 (sp)
    ld         sp, 48(a0)
# 恢复全局指针 (gp) 和 线程指针 (tp)
    ld         gp, 56(a0)
    ld         tp, 64(a0)

# 恢复所有的临时寄存器 (t0-t2)
    ld         t0, 72(a0)
    ld         t1, 80(a0)
    ld         t2, 88(a0)

# 恢复所有的保存寄存器 (s0-s1)
    ld         s0, 96(a0)
    ld         s1, 104(a0)

# -------------------------------------------------------------
# 【极度关键】：跳过 a0 (偏移 112)，留到最后！
# 因为我们现在还需要用 a0 作为基地址去读内存。
# 而这个 a0 里，正装载着 syscall 的返回值（比如 getpid() 的 1）！
# -------------------------------------------------------------

# 恢复其他的参数/返回值寄存器 (a1-a7)
    ld         a1, 120(a0)
    ld         a2, 128(a0)
    ld         a3, 136(a0)
    ld         a4, 144(a0)
    ld         a5, 152(a0)
    ld         a6, 160(a0)
    ld         a7, 168(a0)

# 恢复剩余的保存寄存器 (s2-s11)
    ld         s2, 176(a0)
    ld         s3, 184(a0)
    ld         s4, 192(a0)
    ld         s5, 200(a0)
    ld         s6, 208(a0)
    ld         s7, 216(a0)
    ld         s8, 224(a0)
    ld         s9, 232(a0)
    ld         s10, 240(a0)
    ld         s11, 248(a0)

# 恢复剩余的临时寄存器 (t3-t6)
    ld         t3, 256(a0)
    ld         t4, 264(a0)
    ld         t5, 272(a0)
    ld         t6, 280(a0)

# -------------------------------------------------------------
# 所有的寄存器都恢复完毕了，现在我们不再需要基地址了。
# 把 a0 恢复出来！（这就是那个我们要送给用户的返回值）
# -------------------------------------------------------------
    ld         a0, 112(a0)

# 终极一跃：切换回用户态！
    sret
